const mongoose = require("mongoose");

const AdminBlogSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    tags: {
      type: [String],
      default: [],
    },
  },
  {
    timestamps: true,
  }
);

const AdminBlogModel = mongoose.model("adminblogs", AdminBlogSchema);

module.exports = AdminBlogModel;
